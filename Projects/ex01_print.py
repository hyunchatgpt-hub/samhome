print( "나는 {}입니다".format('손흥민')  )
print( "당신의 점수는 {}점입니다.".format(80)   )
print( "나의 이름은 {}이고, 나이는 {}살입니다.".format('sam', 20) ) #개수와 순서 맞아야 함.
print( "{} x {} = {}".format(5, 3, 5*3)  )
print()

